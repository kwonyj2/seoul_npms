class Command(BaseCommand):
    help = '2026년 감리 초기 데이터 생성'

    def add_arguments(self, parser):
        parser.add_argument('--reset', action='store_true', help='기존 데이터 삭제 후 재생성')

    def handle(self, *args, **options):
        if options['reset']:
            AuditProject.objects.filter(year=2026).delete()
            self.stdout.write('기존 2026년 데이터 삭제 완료')

        # 1. 프로젝트
        project, _ = AuditProject.objects.get_or_create(
            year=2026,
            defaults={
                'name':       '2026년 학교 디지털 인프라 통합관리(테크센터) 운영',
                'audit_firm': '(주)한국정보화기술원',
                'contractor': '',
                'start_date': '2026-05-01',
                'end_date':   '2026-12-31',
                'is_active':  True,
                'is_awarded': False,
            }
        )
        self.stdout.write(f'✓ 프로젝트: {project.name}')

        # 2. 요구사항
        req_map = {}
        for cat, code, name, desc, sla in REQUIREMENTS:
            req, _ = Requirement.objects.get_or_create(
                project=project, code=code,
                defaults={
                    'category':      cat,
                    'name':          name,
                    'description':   desc,
                    'sla_target':    sla,
                    'is_additional': False,
                    'status':        'not_started',
                }
            )
            req_map[code] = req
        self.stdout.write(f'✓ 요구사항 {len(req_map)}개')

        # 3. 산출물 템플릿
        tmpl_count = 0
        pc = {'initiation': 0, 'midterm': 0, 'closing': 0, 'all': 0}
        for (phase, timing, cat, code, name, desc, req_code, is_req, seq) in ARTIFACT_TEMPLATES:
            ArtifactTemplate.objects.get_or_create(
                project=project, code=code,
                defaults={
                    'requirement':   req_map.get(req_code),
                    'audit_phase':   phase,
                    'submit_timing': timing,
                    'category':      cat,
                    'name':          name,
                    'description':   desc,
                    'is_required':   is_req,
                    'is_additional': False,
                    'seq':           seq,
                }
            )
            tmpl_count += 1
            pc[phase] = pc.get(phase, 0) + 1
        self.stdout.write(f'✓ 산출물 템플릿 {tmpl_count}개')

        # 4. 감리 계획
        plan_map = {}
        for phase, p_start, p_end, note in AUDIT_PLANS:
            plan, _ = AuditPlan.objects.get_or_create(
                project=project, phase=phase,
                defaults={
                    'status':        'planned',
                    'planned_start': p_start,
                    'planned_end':   p_end,
                    'notes':         note,
                }
            )
            plan_map[phase] = plan
            self.stdout.write(f'✓ 감리계획: {plan.get_phase_display()}')

        # 5. 체크리스트
        chk_count = 0
        for (phase_key, area, phase_type, seq, desc, check_pt, req_code) in CHECKLIST:
            plan = plan_map.get(phase_key)
            if not plan:
                continue
            ChecklistItem.objects.get_or_create(
                audit_plan=plan, area=area, phase=phase_type, seq=seq,
                defaults={
                    'requirement': req_map.get(req_code),
                    'description': desc,
                    'check_point': check_pt,
                    'result':      'not_checked',
                }
            )
            chk_count += 1
        self.stdout.write(f'✓ 체크리스트 {chk_count}개')

        self.stdout.write('')
        self.stdout.write('══════════════════════════════════════')
        self.stdout.write('  2026년 감리 초기 데이터 생성 완료')
        self.stdout.write('══════════════════════════════════════')
        self.stdout.write(f'  요구사항:      {len(req_map)}개')
        self.stdout.write(f'  산출물 템플릿: {tmpl_count}개')
        self.stdout.write(f'    착수감리 {pc["initiation"]}개 | 중간감리 {pc["midterm"]}개 | 종료감리 {pc["closing"]}개 | 전단계 {pc["all"]}개')
        self.stdout.write(f'  체크리스트:    {chk_count}개')
        ic = sum(1 for c in CHECKLIST if c[0] == 'initiation')
        mc = sum(1 for c in CHECKLIST if c[0] == 'midterm')
        cc = sum(1 for c in CHECKLIST if c[0] == 'closing')
        self.stdout.write(f'    착수 {ic}개 | 중간 {mc}개 | 종료 {cc}개')
